app.factory("ScholarshipApplication", function($resource) {

  return $resource( api + "scholarship_applications/:id", { id: '@id', search: '@search' }, {

    query: { method: 'GET', isArray: false },

    update: { method: 'PUT' },

    search: { method: 'GET' },

  });

});

